;堆栈，用于存储CX标记，多次循环，延长时间
STACK SEGMENT
      DW 0,0,0,0,0,0,0,0
STACK ENDS

CODE SEGMENT
ASSUME CS:CODE,SS:STACK
ORG 1000H
START:
  MOV AX,STACK
  MOV SS,AX
  JMP L
;8255A状态字调为全输出
L:
  MOV AL,80H
  MOV DX,0073H
  OUT DX,AL

  
;灯全亮，测试哪个灯泡有问题

LL0:
   MOV AL,00000000B
   MOV DX,0072H
   OUT DX,AL
   MOV AL,00000000B
   MOV DX,0071H
   OUT DX,AL
   MOV CX,60000
   LOOP $

;灯全灭
LL:
   MOV AL,11111111B
   MOV DX,0072H
   OUT DX,AL
   MOV AL,00001111B
   MOV DX,0071H
   OUT DX,AL
   MOV CX,60000
   LOOP $
   
;接下来的方向输入的信号取决于你的连线，AL中的内容不固定 
;DX的值取决于输出口  
;南北绿 东西红
L1:
   MOV AL,10011110B
   MOV DX,0072H
   OUT DX,AL
   MOV AL,00000111B
   MOV DX,0071H
   OUT DX,AL
   MOV CX,60000
   CALL DO_LOOP
   MOV AH,0
   
;南北黄 东西红
L2:MOV AL,01011101B
   MOV DX,0072H
   OUT DX,AL
   MOV AL,00000111B
   MOV DX,0071H
   OUT DX,AL
   MOV CX,6000
   CALL DO_LOOP
   
   MOV AL,11011111B
   MOV DX,0072H
   OUT DX,AL
   ;MOV AL,00001110B
   MOV AL,00000111B
   MOV DX,0071H
   OUT DX,AL
   MOV CX,6000
   CALL DO_LOOP
   
   ; SHINE 3 TIMES
   INC AH
   CMP AH,3
   JNA L2

   ;东西绿色，南北红
L3:MOV AL,11110011B
   MOV DX,0072H
   OUT DX,AL
   ;MOV AL,00000011B
   MOV AL,00001100B
   MOV DX,0071H
   OUT DX,AL
   MOV CX,60000
   CALL DO_LOOP
   
   MOV AH,0
   
;东西黄，南北红
L4:MOV AL,11101011B
   MOV DX,0072H
   OUT DX,AL
   ;MOV AL,00000101B
   MOV AL,00001010B
   MOV DX,0071H
   OUT DX,AL
   CALL DO_LOOP


;东西黄灭 南北红
   ;MOV AL,11011111B
   MOV AL,11111011B
   MOV DX,0072H
   OUT DX,AL
   ;MOV AL,00000111B
   MOV AL,00001110B
   MOV DX,0071H
   OUT DX,AL
   CALL DO_LOOP
   ; SHINE 3 TIMES
   INC AH
   CMP AH,3
   JNA L4
   
   JMP L1
   
;循环子程序
DO_LOOP PROC NEAR
   PUSH CX
   MOV CX,15
   LOOP $
   POP CX
   LOOP DO_LOOP
   RET
DO_LOOP ENDP
 
CODE ENDS
END START
   

